#include <bits/stdc++.h>
using namespace std;

int main()
{
    // min
    cout << min(10, 100) << "\n";
    cout << min(5.2, 5.1) << "\n";
    cout << min({23, 33, 1, 3, 55, 0, -1}) << "\n";

    // max
    cout << max(10, 100) << "\n";
    cout << max(5.2, 5.1) << "\n";
    cout << max({23, 33, 1, 3, 55, 0, -1}) << "\n";
}