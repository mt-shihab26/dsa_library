#include <bits/stdc++.h>
using namespace std;

int main(void)
{
     double PI = acos(-1);

     // [Mathematics] sin x = sin(x) [In C++ Programming]
     cout << "sin 30 degree or sin PI/6 radian = " << sin(PI / 6) << "\n";

     double PI = acos(-1);
     // [Mathematics] cos x = cos(x) [In C++ Programming]
     cout << "cos 30 degree or cos PI/6 radian = " << cos(PI / 6) << "\n";

     double PI = acos(-1.0);

     // [Mathematics] tan x = tan(x) [In C++ Programming]
     cout << "tan 30 degree or tan PI/6 radian = " << tan(PI / 6) << "\n";
}